class TreeNode {
      
      int val;
      TreeNode(int val){
          this.val=val;
      }
}

class Pair{
    
    TreeNode node;
    int idx;
    
    Pair(TreeNode node,int idx){
        this.idx=idx;
        this.node=node;
    }
}

public class Main
{
	public static void main(String[] args) {
	    
	    
	    
	    TreeNode t1=new TreeNode(5);
	    TreeNode t2=new TreeNode(15);
	    TreeNode t3=new TreeNode(50);
	    TreeNode t4=new TreeNode(55);
	    
	    Pair p1=new Pair(t1,10);
	    Pair p2=new Pair(t2,30);
	    Pair p3=new Pair(t3,100);
	    Pair p4=new Pair(t4,110);
	    
		System.out.println(t1+" "+t1.val+" "+p1.node+" "+p1.node.val);
		System.out.println(t2+" "+t2.val+" "+p2.node+" "+p2.node.val);
		System.out.println(t3+" "+t3.val+" "+p3.node+" "+p3.node.val);
		System.out.println(t4+" "+t4.val+" "+p4.node+" "+p4.node.val);
	}
}


